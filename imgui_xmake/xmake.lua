    set_kind("binary")
    add_files("src/*.cpp")
    add_files("imgui/*.cpp")
    add_files("imgui/backends/imgui_impl_glfw.cpp","imgui/backends/imgui_impl_opengl3.cpp")

    add_includedirs("src", "imgui", "imgui/backends", "imgui/freetype2")

    set_toolchains("mingw")
    -- now use `xmake f -p windows --toolchain=clang`
    
